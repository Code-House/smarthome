package org.eclipse.smarthome.io.http.core.internal.filter;

import java.io.IOException;

import javax.servlet.Filter;
import javax.servlet.FilterChain;
import javax.servlet.FilterConfig;
import javax.servlet.ServletException;
import javax.servlet.ServletRequest;
import javax.servlet.ServletResponse;
import javax.servlet.http.HttpServlet;

/**
 * Filter which wraps servlet execution.
 *
 * @author ≈Åukasz Dywicki
 */
public class ServletDelegatingFilter implements Filter {

    private final HttpServlet servlet;

    public ServletDelegatingFilter(HttpServlet servlet) {
        this.servlet = servlet;
    }

    @Override
    public void init(FilterConfig filterConfig) throws ServletException {

    }

    @Override
    public void doFilter(ServletRequest request, ServletResponse response, FilterChain chain)
            throws IOException, ServletException {
        servlet.service(request, response);
    }

    @Override
    public void destroy() {

    }

}